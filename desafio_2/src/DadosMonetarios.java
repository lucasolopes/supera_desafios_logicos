
public class DadosMonetarios {
	private String valor;
	private String tipo;
	private String tipoValor;

	public DadosMonetarios(String valor, String tipo, String string) {
		super();
		this.valor = valor;
		this.tipo = tipo;
		this.tipoValor = string;
	}

	public String getValor() {
		return valor;
	}

	public String getTipo() {
		return tipo;
	}

	public String getTipoValor() {
		return tipoValor;
	}

}
